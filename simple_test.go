func TestLibraryLoading(t *testing.T) {
	// Try to run Pngquant with valid data
	data := []byte{
		0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
		0x00, 0x00, 0x00, 0x0D, // IHDR length
		0x49, 0x48, 0x44, 0x52, // IHDR
		0x00, 0x00, 0x00, 0x01, // width = 1
		0x00, 0x00, 0x00, 0x01, // height = 1
		0x08, 0x06, 0x00, 0x00, 0x00, // bit depth, color type, etc
		0x1F, 0x15, 0xC4, 0x89, // CRC
		0x00, 0x00, 0x00, 0x0A, // IDAT length
		0x49, 0x44, 0x41, 0x54, // IDAT
		0x78, 0xDA, 0x62, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, // compressed data
		0x00, 0x00, 0x00, 0x00, // IEND length
		0x49, 0x45, 0x4E, 0x44, // IEND
		0xAE, 0x42, 0x60, 0x82, // CRC
	}

	_, err := Pngquant(data)
	if err != nil {
		t.Logf("Pngquant returned error (this might be expected): %v", err)
	} else {
		t.Log("Pngquant executed successfully")
	}
}
